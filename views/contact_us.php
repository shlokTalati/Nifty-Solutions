<script>
    document.addEventListener("ajaxFormSubmitResponse", function(event) {
        const response = event.detail;

        if (!response || !response.alert || !response.message) return;

        // Remove existing alert (if any)
        const oldAlert = document.querySelector(".custom-form-alert");
        if (oldAlert) oldAlert.remove();

        // Create new alert div
        const alertDiv = document.createElement("div");
        alertDiv.className = `alert ${response.alert} custom-form-alert alert-dismissible fade show mt-4`;
        alertDiv.role = "alert";
        alertDiv.innerHTML = `
        ${response.message}
        <button type="button" class="btn-close" data-bs-dismiss="alert" aria-label="Close"></button>
    `;

        // Insert alert after form
        const form = document.querySelector("form");
        if (form && form.parentNode) {
            form.parentNode.insertBefore(alertDiv, form.nextSibling);
        }

        // ✅ Clear form fields ONLY if submission was successful
        if (response.alert.includes("alert-success") && form) {
            form.reset(); // Clears all standard inputs, textareas, etc.

            // Optional: manually clear any weird custom fields if needed
            form.querySelectorAll("input, textarea").forEach(input => {
                input.value = "";
            });
        }
    });
</script>